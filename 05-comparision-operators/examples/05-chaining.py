#!/usr/bin/env python3

# comparision operators is expressions to evaluate
# values if they are True or False.
# Typically used in if statements.

# chaining multiple comparisions
print("2 equal to 2 or 1 equal to 2:", 2 == 2 or 1 == 2)  # True
print("'ho' equal to 'ho' and 1 equal to '1':",
      "ho" == "ho" and 1 == "1")  # False
